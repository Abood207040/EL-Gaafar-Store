const LocalizationContext = createContext(null);

export function LocalizationProvider({ children }) {
  const [language, setLanguage] = useState(() => {
    try {
      return window.localStorage.getItem(STORAGE_KEY) || 'ar';
    } catch {
      return 'ar';
    }
  });

  useEffect(() => {
    const dir = language === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = language;
    document.documentElement.dir = dir;
    try {
      window.localStorage.setItem(STORAGE_KEY, language);
    } catch {
      // Ignore storage errors in restricted environments.
    }
  }, [language]);

  const value = useMemo(() => {
    const isArabic = language === 'ar';
    const t = (key, ...args) => {
      const entry = messages[language]?.[key] ?? messages.en[key] ?? key;
      return typeof entry === 'function' ? entry(...args) : entry;
    };

    const translateCategory = (category) =>
      isArabic ? CATEGORY_LABELS[category] || category : category;
    const translateStock = (status) =>
      isArabic ? STOCK_LABELS[status] || status : status;
    const translateOrderStatus = (status) =>
      isArabic ? ORDER_LABELS[status] || status : status;
    const translateFulfillment = (method) =>
      isArabic ? FULFILLMENT_LABELS[method] || method : method;
    const translateSpecLabel = (label) =>
      isArabic ? SPEC_LABELS[label] || SPEC_KEY_LABELS[label] || label : label;
    const translateSpecValue = (value) =>
      isArabic ? SPEC_VALUE_AR[value] || value : value;
    const productName = (product) => (isArabic ? product.nameAr || product.nameEn : product.nameEn);
    const productAltName = (product) => (isArabic ? product.nameEn : product.nameAr);
    const productDescription = (product) =>
      isArabic ? PRODUCT_DESCRIPTIONS_AR[product.id] || product.description : product.description;
    const customerStatus = (status) => {
      const key = {
        Active: 'customerStatusActive',
        New: 'customerStatusNew',
        Inactive: 'customerStatusInactive',
      }[status];
      return key ? t(key) : status;
    };
    const timelineStep = (step) => {
      const key = {
        Placed: 'timelinePlaced',
        Confirmed: 'timelineConfirmed',
        Processing: 'timelineProcessing',
        Shipped: 'timelineShipped',
        'Ready for Pickup': 'timelineReady',
        Delivered: 'timelineDelivered',
        Cancelled: 'timelineCancelled',
      }[step];
      return key ? t(key) : step;
    };

    return {
      language,
      dir: isArabic ? 'rtl' : 'ltr',
      isArabic,
      setLanguage,
      toggleLanguage: () => setLanguage((current) => (current === 'ar' ? 'en' : 'ar')),
      t,
      translateCategory,
      translateStock,
      translateOrderStatus,
      translateFulfillment,
      translateSpecLabel,
      translateSpecValue,
      productName,
      productAltName,
      productDescription,
      customerStatus,
      timelineStep,
    };
  }, [language]);

  return (
    <LocalizationContext.Provider value={value}>
      {children}
    </LocalizationContext.Provider>
  );
}

// eslint-disable-next-line react-refresh/only-export-components
export function useLocalization() {
  const context = useContext(LocalizationContext);
  if (!context) {
    throw new Error('useLocalization must be used inside LocalizationProvider');
  }
  return context;
}
